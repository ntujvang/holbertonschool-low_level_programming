/**
 * op_add - add!
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract!
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply!
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide!
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo!
 * @a: first num
 * @b: second num
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
